Byte * SearchDataToCache(Byte *data) {
    int organizer = data[0];
    int tie = data[1];
    int alongStraight = data[2];
    if (!organizer) return data + alongStraight;
    for (int i = 0; i < tie / 2; i++) {
        int begin = alongStraight + i;
        int end = alongStraight + tie - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[alongStraight + tie] = 0;
    return data + alongStraight;
}

NSString *StringFromSearchData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SearchDataToCache(data)];
}  
