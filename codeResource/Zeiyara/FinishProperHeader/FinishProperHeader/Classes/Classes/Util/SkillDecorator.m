Byte * PartyDataToCache(Byte *data) {
    int quantityeraction = data[0];
    int gestureTravel = data[1];
    int documentRecovery = data[2];
    if (!quantityeraction) return data + documentRecovery;
    for (int i = 0; i < gestureTravel / 2; i++) {
        int begin = documentRecovery + i;
        int end = documentRecovery + gestureTravel - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[documentRecovery + gestureTravel] = 0;
    return data + documentRecovery;
}

NSString *StringFromPartyData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PartyDataToCache(data)];
}  
