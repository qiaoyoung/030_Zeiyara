Byte * PublishDataToCache(Byte *data) {
    int counteractionAutomat = data[0];
    int society = data[1];
    Byte tensionQuantityeraction = data[2];
    int tale = data[3];
    if (!counteractionAutomat) return data + tale;
    for (int i = tale; i < tale + society; i++) {
        int value = data[i] - tensionQuantityeraction;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[tale + society] = 0;
    return data + tale;
}

NSString *StringFromPublishData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PublishDataToCache(data)];
}
